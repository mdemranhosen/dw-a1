#!/bin/bash
# Generates German pronunciation audio (M4A/AAC) using the macOS `say` command
# with the German voice "Anna". Output goes into ./audio/ next to the markdown.
#
# Re-run any time after adding new entries.

set -e

VOICE="Anna"
ROOT_DIR="$(cd "$(dirname "$0")/.." && pwd)"
AUDIO_DIR="$ROOT_DIR/audio"

