         "$AUDIO_DIR/phrases/welcome" \
         "$AUDIO_DIR/phrases/chapter1" \
         "$AUDIO_DIR/phrases/chapter2"

gen() {
  # gen <output_path> <german_text>
  local out="$1"; shift
  local text="$*"
  if [ ! -f "$out" ]; then
    say -v "$VOICE" -o "$out" --data-format=aac "$text"
    echo "  + $out"
  fi
}

